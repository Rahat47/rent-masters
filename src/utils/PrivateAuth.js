import { Redirect, Route } from "react-router";

export default function PrivateRoute({ children, ...rest }) {
    let auth = true
    return (
        <Route
            {...rest}
            render={({ location }) =>
                auth ? (
                    children
                ) : (
                    <Redirect
                        to={{
                            pathname: "/auth",
                            state: { from: location }
                        }}
                    />
                )
            }
        />
    );
}